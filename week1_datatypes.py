#########################Data types in Python#########################
# String
name = "Ragu"
print("The name is " + name)
# Integer
age = 39
print("The age is " + str(age))
# Float
height = 5.9
print("The height is " + str(height))
# Boolean
is_student = True
print("Is the person a student? " + str(is_student))
# List
fruits = ["apple", "banana", "cherry"]
print("The fruits are " + str(fruits))
# Tuple
coordinates = (10, 20)
print("The coordinates are " + str(coordinates))
# Dictionary
person = {"name": "Ragu", "age": 39, "height": 5.9}
print("The person details are " + str(person))
# Set
unique_numbers = {1, 2, 3, 4, 5}
print("The unique numbers are " + str(unique_numbers))
